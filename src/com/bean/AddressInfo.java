package com.bean;

public class AddressInfo {
	// 地址ID
	private String appreceiveaddressid;
	// 用户ID
	private String userid;
	// 用户名
	private String username;
	// 收货人姓名
	private String receivename;
	// 电话号码
	private String phonenumber;
	// 省份
	private String province;
	// 城市
	private String city;
	// 地区
	private String area;
	// 详细地址
	private String address;
	// 默认地址标志
	private String sign;

	public String getAppreceiveaddressid() {
		return appreceiveaddressid;
	}

	public void setAppreceiveaddressid(String appreceiveaddressid) {
		this.appreceiveaddressid = appreceiveaddressid;
	}

	public String getUserid() {
		return userid;
	}

	public void setUserid(String userid) {
		this.userid = userid;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getReceivename() {
		return receivename;
	}

	public void setReceivename(String receivename) {
		this.receivename = receivename;
	}

	public String getPhonenumber() {
		return phonenumber;
	}

	public void setPhonenumber(String phonenumber) {
		this.phonenumber = phonenumber;
	}

	public String getProvince() {
		return province;
	}

	public void setProvince(String province) {
		this.province = province;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getSign() {
		return sign;
	}

	public void setSign(String sign) {
		this.sign = sign;
	}

}
