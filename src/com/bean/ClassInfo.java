package com.bean;

public class ClassInfo {
	// 商品管理中类id
	private String midclassid;
	// 商品管理中类名字
	private String midclassname;
	// 商品管理小类id
	private String minclassid;
	// 商品管理小类名字
	private String minclassname;
	// 药品标志
	private String drugflag;
	// 小类图片商品
	private String goodsid;
	// 小类图片名字
	private String picname;
	// 图片文件夹地址
	private String folderpath;

	public String getMidclassid() {
		return midclassid;
	}

	public void setMidclassid(String midclassid) {
		this.midclassid = midclassid;
	}

	public String getMidclassname() {
		return midclassname;
	}

	public void setMidclassname(String midclassname) {
		this.midclassname = midclassname;
	}

	public String getMinclassid() {
		return minclassid;
	}

	public void setMinclassid(String minclassid) {
		this.minclassid = minclassid;
	}

	public String getMinclassname() {
		return minclassname;
	}

	public void setMinclassname(String minclassname) {
		this.minclassname = minclassname;
	}

	public String getDrugflag() {
		return drugflag;
	}

	public void setDrugflag(String drugflag) {
		this.drugflag = drugflag;
	}

	public String getGoodsid() {
		return goodsid;
	}

	public void setGoodsid(String goodsid) {
		this.goodsid = goodsid;
	}

	public String getPicname() {
		return picname;
	}

	public void setPicname(String picname) {
		this.picname = picname;
	}

	public String getFolderpath() {
		return folderpath;
	}

	public void setFolderpath(String folderpath) {
		this.folderpath = folderpath;
	}

}
