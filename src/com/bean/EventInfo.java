package com.bean;

import java.util.List;

public class EventInfo {

	// 活动ID
	private String eventid;
	// 活动名称
	private String eventname;
	// 活动内容
	private String eventdesc;
	// 链接方式
	private String linktype;
	// 商品ID
	private String goodsid;
	// 商品名称
	private String goodsname;
	// 链接活动id
	private String linkeventid;
	// 自定义链接
	private String deflink;
	// 图片名称
	private String picname;
	// 图片
	private List piclist;
	// 图片文件夹地址
	private String folderpath;

	public String getEventid() {
		return eventid;
	}

	public void setEventid(String eventid) {
		this.eventid = eventid;
	}

	public String getEventname() {
		return eventname;
	}

	public void setEventname(String eventname) {
		this.eventname = eventname;
	}

	public String getEventdesc() {
		return eventdesc;
	}

	public void setEventdesc(String eventdesc) {
		this.eventdesc = eventdesc;
	}

	public String getLinktype() {
		return linktype;
	}

	public void setLinktype(String linktype) {
		this.linktype = linktype;
	}

	public String getGoodsid() {
		return goodsid;
	}

	public void setGoodsid(String goodsid) {
		this.goodsid = goodsid;
	}

	public String getGoodsname() {
		return goodsname;
	}

	public void setGoodsname(String goodsname) {
		this.goodsname = goodsname;
	}

	public String getLinkeventid() {
		return linkeventid;
	}

	public void setLinkeventid(String linkeventid) {
		this.linkeventid = linkeventid;
	}

	public String getDeflink() {
		return deflink;
	}

	public void setDeflink(String deflink) {
		this.deflink = deflink;
	}

	public String getPicname() {
		return picname;
	}

	public void setPicname(String picname) {
		this.picname = picname;
	}

	public List getPiclist() {
		return piclist;
	}

	public void setPiclist(List piclist) {
		this.piclist = piclist;
	}

	public String getFolderpath() {
		return folderpath;
	}

	public void setFolderpath(String folderpath) {
		this.folderpath = folderpath;
	}

}
